package main

import (
	"fmt"
)

func main() {
	a := [...]int{23, 43, 1, 23, 67, 88, 90, 34, 56, 37, 44, 26}
	// [1, 23, 43, 23, 67, 88, 90, 34, 56, 37, 44, 26]
	// [1],[23],[23, 34, 37, 26, 43, 67, 56, 88, 44, 90]
	// [1],[23],[23, 34, 37, 26],[43],[67, 56, 88, 44, 90]
	// [1],[23],[23],[34,37,26],[43],[67,56,88,44,90]
	// [1],[23],[23],[26],[34],[37],[43],[56,44],[67],[88,90]
	// [1],[23],[23],[34],[26],[34],[37],[44],[56],[67],[88],[90]
	qsort(a[:], 0, len(a)-1)
	fmt.Println(a)
}

func qsort(b []int, left, right int) {
	if left >= right {
		return
	}
	val := b[left]
	k := left
	for i := left + 1; i <= right; i++ {
		if b[i] < val {
			b[k] = b[i]
			b[i] = b[k+1]
			k++
		}
	}
	b[k] = val
	fmt.Println(b)
	qsort(b, left, k-1)
	qsort(b, k+1, right)
}
